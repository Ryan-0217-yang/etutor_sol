n = int(input())
grade = []

for i in range(n):
    grade.append(int(input()))

maxgrade = max(grade)
mingrade = min(grade)
passnum = sum(1 for g in grade if g >= 60)
average = sum(grade) / n

print(f"Max:{maxgrade}")
print(f"Min:{mingrade}")
print(f"Average:{average:.1f}")
print(f"Pass:{passnum}")
